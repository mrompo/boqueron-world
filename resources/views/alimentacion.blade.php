<p><strong>Propiedades nutritivas y salud</strong></p>
<p style="text-align: left;">El boquer&oacute;n es un pescado azul que contiene 6 gramos de grasa por cada 100 gramos de porci&oacute;n comestible, similar a las sardinas, el jurel o el at&uacute;n. No obstante, al igual que ocurre con estas otras especies, la grasa presenta un alto contenido en &aacute;cidos omega-3.</p>
<p style="text-align: left;">Como el resto de pescados, es una fuente de prote&iacute;nas de alto valor biol&oacute;gico, destacando adem&aacute;s vitaminas de los grupos B, A y D.</p>
<p style="text-align: left;">En el grupo B sobresalen la B2, B3, B6, B9 y B12, que activan el sistema defensivo y nervioso, adem&aacute;s de facilitar el aprovechamiento de nutrientes como los hidratos de carbono, grasas y prote&iacute;nas.</p>
<p style="text-align: left;">Los grupos de vitaminas A y D, liposolubles, contribuyen a una mejor resistencia contra las infecciones y mantienen los tejidos. La vitamina A, adem&aacute;s, act&uacute;a en el correcto funcionamiento de &oacute;rganos vitales como el h&iacute;gado, mientras que la D absorbe el calcio fij&aacute;ndolo en los huesos.</p>
<p style="text-align: left;">Los minerales relevantes presentes en el boquer&oacute;n son magnesio, yodo y hierro. El primero posee un ligero efecto laxante y favorece el correcto funcionamiento de intestinos, m&uacute;sculos y nervios. El yodo se relaciona con la regulaci&oacute;n de la gl&aacute;ndula tiroides y sus numerosas funciones metab&oacute;licas como el crecimiento y desarrollo del cerebro en el feto. Finalmente, el hierro act&uacute;a en relaci&oacute;n a la hemoglobina por lo que facilita el transporte de ox&iacute;geno a las c&eacute;lulas.</p>
<p style="text-align: left;">Son destacables adem&aacute;s los aportes de calcio que proporciona el boquer&oacute;n o la anchoa si se comen con espinas, proporcionando 10 gramos de esta especie el mismo calcio que un vaso de leche.</p>
